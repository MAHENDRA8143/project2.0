    add_heading(doc, '4. Problem Statement', level=1)
    add_paragraph(doc, '4.1 Real-world Problem')
    add_paragraph(doc, 'Operators need continuous, actionable insights for rapidly evolving process conditions. Absent these, transient events can ' \
                  'cause violations or downstream failures.')
