    add_heading(doc, '11. Advantages of the System', level=1)
    add_bullets(doc, ['Real-time monitoring', 'Early warning', 'Cost reduction', 'Automation and consistency'])

    add_heading(doc, '12. Applications', level=1)
    add_bullets(doc, ['Municipal WWTPs', 'Smart city integrations', 'Industrial discharge monitoring'])

    add_heading(doc, '13. Future Scope', level=1)
