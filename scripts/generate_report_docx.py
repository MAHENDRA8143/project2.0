from docx import Document
from docx.shared import Pt
from docx.enum.text import WD_PARAGRAPH_ALIGNMENT
from datetime import datetime
import os

OUTPUT_DIR = os.path.join(os.path.dirname(__file__), '..', 'docs')
OUTPUT_PATH = os.path.join(OUTPUT_DIR, 'AI_Based_Smart_WWTP_Report.docx')

REPORT_TITLE = 'AI-Based Smart Wastewater Treatment Plant Prediction and Monitoring System'
AUTHOR = 'Generated by project tooling'

LONG_INTRO = (
    "This project develops an end-to-end platform for forecasting and monitoring wastewater treatment plant "
    "parameters at hourly resolution. It integrates data simulation, preprocessing, feature engineering, an ensemble "
    "of predictive models (statistical, tree-based, and deep sequence networks), an alerting engine, and a web-based "
    "dashboard with reporting capabilities. The document below details background, methods, implementation, results, "
    "and future work in a style suitable for academic submission."
)

def add_heading(doc, text, level=1):
    doc.add_heading(text, level=level)


def add_paragraph(doc, text, bold=False, italic=False):
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.bold = bold
    run.italic = italic
    p.paragraph_format.space_after = Pt(6)
    return p


def add_bullets(doc, items):
    for it in items:
        p = doc.add_paragraph(it, style='List Bullet')
        p.paragraph_format.space_after = Pt(2)


def ensure_output_dir():
    os.makedirs(OUTPUT_DIR, exist_ok=True)


