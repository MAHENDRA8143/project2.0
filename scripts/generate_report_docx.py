    add_heading(doc, '5. Proposed System', level=1)
    add_paragraph(doc, '5.1 System Overview')
    add_paragraph(doc, 'A modular pipeline integrates simulation, ingestion, preprocessing, feature engineering, modeling, alerting and visualization. ')

